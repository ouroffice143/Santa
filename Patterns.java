package com.reg.java;

public class Patterns {

	public static void main(String[] args) {
		int j=0;
		for (int i = 1; i < 5; i++) 
		{
			for (j = 0; j < i; j++) 
			{
				System.out.println(" ");
				
			for (j = 0; j < i; j++) 
			{
			
				System.out.print(i + " ");
			}
			}
			System.out.print(" ");
		}
		

	}
}	
		
	
	


